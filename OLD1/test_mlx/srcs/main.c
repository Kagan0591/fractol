#include "fractol.h"

int	key_hook(int keycode, t_vars *vars)
{
	(void) vars;
	printf("Hello from key_hook!\nkeycode is %d\n", keycode);
	return (0);
}

int	main(void)
{
	t_vars	vars;

	vars.mlx = mlx_init();
	vars.win = mlx_new_window(vars.mlx, 640, 480, "Hello world!");
	mlx_key_hook(vars.win, key_hook, &vars);
	mlx_loop(vars.mlx);
}